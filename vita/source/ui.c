/* ---- Framebuffer helpers ---- */

static void fb_set_pixel(int x, int y, uint32_t color) {
    if (x >= 0 && x < SCREEN_W && y >= 0 && y < SCREEN_H)
        g_fb[y * SCREEN_W + x] = color;
}

static void fb_fill(uint32_t color) {
    int total = SCREEN_W * SCREEN_H;
    for (int i = 0; i < total; i++) g_fb[i] = color;
}

static void fb_draw_char(int col, int row, char c, uint32_t fg, uint32_t bg) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font8x8[(unsigned char)c - 32];
    int px = col * FONT_W;
    int py = row * FONT_H;
    for (int r = 0; r < FONT_H; r++) {
        uint8_t bits = glyph[r];
        for (int b = 0; b < FONT_W; b++)
            fb_set_pixel(px + b, py + r, (bits & (0x80 >> b)) ? fg : bg);
    }
}

static void fb_draw_str(int col, int row, const char *s, uint32_t fg, uint32_t bg) {
    while (*s && col < COLS) {
        if (*s == '\n') { row++; col = 0; s++; continue; }
        fb_draw_char(col++, row, *s++, fg, bg);
    }
}

static void fb_draw_strf(int col, int row, uint32_t fg, uint32_t bg,
                         const char *fmt, ...) {
    char buf[256];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);
    fb_draw_str(col, row, buf, fg, bg);
}

static void fb_flush(void) {
    SceDisplayFrameBuf fb;
    fb.size        = sizeof(SceDisplayFrameBuf);
    fb.base        = g_fb;
    fb.pitch       = SCREEN_W;
    fb.pixelformat = SCE_DISPLAY_PIXELFORMAT_A8B8G8R8;
    fb.width       = SCREEN_W;
    fb.height      = SCREEN_H;
    sceDisplaySetFrameBuf(&fb, SCE_DISPLAY_SETBUF_NEXTFRAME);
}

/* ---- Public UI API ---- */

void ui_init(void) {
    fb_fill(COL_BG);
    fb_flush();
}

void ui_clear(void) {
    fb_fill(COL_BG);
}

void ui_status(const char *fmt, ...) {
    /* Erase status row */
    for (int c = 0; c < COLS; c++)
        fb_draw_char(c, STATUS_ROW, ' ', COL_FG, COL_BG);

    char buf[256];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);

    fb_draw_str(0, STATUS_ROW, buf, COL_STATUS, COL_BG);
    fb_flush();
}

void ui_message(const char *fmt, ...) {
    fb_fill(COL_BG);

    char buf[512];
    va_list args;
    va_start(args, fmt);
    vsnprintf(buf, sizeof(buf), fmt, args);
    va_end(args);

    int row = 2, col = 0;
    for (const char *p = buf; *p; p++) {
        if (*p == '\n') { row++; col = 0; continue; }
        fb_draw_char(col++, row, *p, COL_FG, COL_BG);
        if (col >= COLS) { row++; col = 0; }
    }

    fb_draw_str(0, row + 2, "Press X to continue", COL_HDR, COL_BG);
    fb_flush();

    /* Wait for X (Cross) */
    SceCtrlData pad;
    uint32_t prev = 0;
    while (1) {
        sceCtrlReadBufferPositive2(0, &pad, 1);
        uint32_t just = pad.buttons & ~prev;
        prev = pad.buttons;
        if (just & SCE_CTRL_CROSS) break;
        sceKernelDelayThread(16000);
    }
}

void ui_draw_list(const SyncState *state, int selected, int scroll) {
    fb_draw_strf(0, 0, COL_HDR, COL_BG,
                 "=== Vita Save Sync v%s ===", APP_VERSION);

    const char *type_str =
        (state->scan_vita_saves && state->scan_psp_emu_saves) ? "Vita+PSP" :
        state->scan_vita_saves ? "Vita only" : "PSP emu only";
    fb_draw_strf(0, 1, COL_FG, COL_BG,
                 "%d saves [%s] | X:Sync Sq:Up Tri:Dn Sel:Scan",
                 state->num_titles, type_str);

    fb_draw_str(0, 2, "-------------------------------------------------------",
                COL_FG, COL_BG);

    int visible = STATUS_ROW - LIST_START_ROW - 2;
    int end = scroll + visible;
    if (end > state->num_titles) end = state->num_titles;

    for (int i = scroll; i < end; i++) {
        const TitleInfo *t = &state->titles[i];
        bool sel = (i == selected);
        uint32_t fg = sel ? COL_SEL_FG : COL_FG;

        char line[COLS + 1];
        const char *plat = t->platform == PLATFORM_PSP_EMU ? "PSP" : "VTA";
        snprintf(line, sizeof(line), "%s %-9s  %-36s",
                 sel ? ">" : " ", t->game_id, t->name);
        fb_draw_str(0, LIST_START_ROW + (i - scroll), line, fg, COL_BG);
    }

    fb_draw_str(0, STATUS_ROW - 1,
                "Up/Dn: nav | L/R: page | Start: exit",
                COL_FG, COL_BG);
    fb_flush();
}

bool ui_confirm(const TitleInfo *title, SyncAction action,
                const char *server_hash, uint32_t server_size) {
    fb_fill(COL_BG);

    const char *action_str =
        action == SYNC_UPLOAD   ? "UPLOAD to server" :
        action == SYNC_DOWNLOAD ? "DOWNLOAD from server" :
        action == SYNC_CONFLICT ? "CONFLICT - manual decision" :
        "UP TO DATE";

    fb_draw_strf(0, 1, COL_HDR, COL_BG, "Game:    %s (%s)", title->name, title->game_id);
    fb_draw_strf(0, 2, COL_FG,  COL_BG, "Platform:%s",
                 title->platform == PLATFORM_PSP_EMU ? " PSP (emulated)" : " PS Vita native");
    fb_draw_strf(0, 3, COL_SEL_FG, COL_BG, "Action:  %s", action_str);
    fb_draw_strf(0, 4, COL_FG, COL_BG, "Local:   %u bytes", title->total_size);
    if (server_hash && server_hash[0])
        fb_draw_strf(0, 5, COL_FG, COL_BG, "Server:  %u bytes", server_size);
    else
        fb_draw_str(0, 5, "Server:  (no save)", COL_FG, COL_BG);

    if (action == SYNC_UP_TO_DATE) {
        fb_draw_str(0, 7, "Already up to date. Press X.", COL_FG, COL_BG);
        fb_flush();
        SceCtrlData pad;
        uint32_t prev = 0;
        while (1) {
            sceCtrlReadBufferPositive2(0, &pad, 1);
            uint32_t just = pad.buttons & ~prev;
            prev = pad.buttons;
            if (just & (SCE_CTRL_CROSS | SCE_CTRL_CIRCLE)) break;
            sceKernelDelayThread(16000);
        }
        return false;
    }

    fb_draw_str(0, 7, "X: Confirm  |  O: Cancel", COL_HDR, COL_BG);
    fb_flush();

    SceCtrlData pad;
    uint32_t prev = 0;
    while (1) {
        sceCtrlReadBufferPositive2(0, &pad, 1);
        uint32_t just = pad.buttons & ~prev;
        prev = pad.buttons;
        if (just & SCE_CTRL_CROSS)  return true;
        if (just & SCE_CTRL_CIRCLE) return false;
        sceKernelDelayThread(16000);
    }
}

