_AES_RCON = (0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36)


def _xtime(a: int) -> int:
    return (((a << 1) ^ 0x1B) & 0xFF) if (a & 0x80) else ((a << 1) & 0xFF)


def _mul(a: int, b: int) -> int:
    result = 0
    x = a
    y = b
    while y:
        if y & 1:
            result ^= x
        x = _xtime(x)
        y >>= 1
    return result


def _bytes_to_state(block: bytes) -> list[list[int]]:
    return [[block[row + 4 * col] for col in range(4)] for row in range(4)]


def _state_to_bytes(state: list[list[int]]) -> bytes:
    return bytes(state[row][col] for col in range(4) for row in range(4))


def _add_round_key(state: list[list[int]], round_key: bytes) -> None:
    for col in range(4):
        for row in range(4):
            state[row][col] ^= round_key[4 * col + row]


def _sub_bytes(state: list[list[int]]) -> None:
    for row in range(4):
        for col in range(4):
            state[row][col] = _AES_SBOX[state[row][col]]


def _inv_sub_bytes(state: list[list[int]]) -> None:
    for row in range(4):
        for col in range(4):
            state[row][col] = _AES_INV_SBOX[state[row][col]]


def _shift_rows(state: list[list[int]]) -> None:
    for row in range(1, 4):
        state[row] = state[row][row:] + state[row][:row]


def _inv_shift_rows(state: list[list[int]]) -> None:
    for row in range(1, 4):
        state[row] = state[row][-row:] + state[row][:-row]


def _mix_columns(state: list[list[int]]) -> None:
    for col in range(4):
        s0, s1, s2, s3 = (state[row][col] for row in range(4))
        state[0][col] = _mul(s0, 2) ^ _mul(s1, 3) ^ s2 ^ s3
        state[1][col] = s0 ^ _mul(s1, 2) ^ _mul(s2, 3) ^ s3
        state[2][col] = s0 ^ s1 ^ _mul(s2, 2) ^ _mul(s3, 3)
        state[3][col] = _mul(s0, 3) ^ s1 ^ s2 ^ _mul(s3, 2)


def _inv_mix_columns(state: list[list[int]]) -> None:
    for col in range(4):
        s0, s1, s2, s3 = (state[row][col] for row in range(4))
        state[0][col] = _mul(s0, 0x0E) ^ _mul(s1, 0x0B) ^ _mul(s2, 0x0D) ^ _mul(s3, 0x09)
        state[1][col] = _mul(s0, 0x09) ^ _mul(s1, 0x0E) ^ _mul(s2, 0x0B) ^ _mul(s3, 0x0D)
        state[2][col] = _mul(s0, 0x0D) ^ _mul(s1, 0x09) ^ _mul(s2, 0x0E) ^ _mul(s3, 0x0B)
        state[3][col] = _mul(s0, 0x0B) ^ _mul(s1, 0x0D) ^ _mul(s2, 0x09) ^ _mul(s3, 0x0E)


def _expand_key(key: bytes) -> list[bytes]:
    if len(key) != 16:
        raise ValueError("AES-128 key must be 16 bytes")
    words: list[list[int]] = [list(key[i : i + 4]) for i in range(0, 16, 4)]
    for i in range(4, 44):
        temp = words[i - 1][:]
        if i % 4 == 0:
            temp = temp[1:] + temp[:1]
            temp = [_AES_SBOX[b] for b in temp]
            temp[0] ^= _AES_RCON[(i // 4) - 1]
        words.append([words[i - 4][j] ^ temp[j] for j in range(4)])
    return [bytes(sum(words[4 * rnd : 4 * (rnd + 1)], [])) for rnd in range(11)]


_AES_ROUND_KEYS = _expand_key(_VMP_AES_KEY)


def _aes_encrypt_block(block: bytes) -> bytes:
    state = _bytes_to_state(block)
    _add_round_key(state, _AES_ROUND_KEYS[0])
    for rnd in range(1, 10):
        _sub_bytes(state)
        _shift_rows(state)
        _mix_columns(state)
        _add_round_key(state, _AES_ROUND_KEYS[rnd])
    _sub_bytes(state)
    _shift_rows(state)
    _add_round_key(state, _AES_ROUND_KEYS[10])
    return _state_to_bytes(state)


def _aes_decrypt_block(block: bytes) -> bytes:
    state = _bytes_to_state(block)
    _add_round_key(state, _AES_ROUND_KEYS[10])
    for rnd in range(9, 0, -1):
        _inv_shift_rows(state)
        _inv_sub_bytes(state)
        _add_round_key(state, _AES_ROUND_KEYS[rnd])
        _inv_mix_columns(state)
    _inv_shift_rows(state)
    _inv_sub_bytes(state)
    _add_round_key(state, _AES_ROUND_KEYS[0])
    return _state_to_bytes(state)


def is_ps1_title_id(title_id: str) -> bool:
    upper = title_id.upper()
    return len(upper) >= 4 and upper[:4] in PS1_PREFIXES


def extract_raw_card(data: bytes) -> bytes:
    if data.startswith(MC_MAGIC):
        if len(data) != RAW_CARD_SIZE:
            raise ValueError(f"Invalid raw PS1 card size: {len(data)}")
        return data
    if data.startswith(VMP_MAGIC):
        if len(data) < VMP_SIZE:
            raise ValueError(f"Invalid VMP size: {len(data)}")
        raw = data[VMP_HEADER_SIZE : VMP_HEADER_SIZE + RAW_CARD_SIZE]
        if not raw.startswith(MC_MAGIC):
            raise ValueError("VMP payload does not contain a valid PS1 card")
        return raw
    raise ValueError("Unsupported PS1 card format")


def slot_raw_name(slot: int) -> str:
    return f"slot{slot}.mcd"


def slot_vmp_name(slot: int) -> str:
    return f"SCEVMC{slot}.VMP"


def get_slot_raw_from_files(files: list[tuple[str, bytes]], slot: int) -> bytes | None:
    preferred = slot_raw_name(slot)
    legacy = slot_vmp_name(slot)
    for path, data in files:
        if path == preferred:
            return extract_raw_card(data)
    for path, data in files:
        if path == legacy:
            return extract_raw_card(data)
    return None


def ensure_raw_slot_files(files: list[tuple[str, bytes]]) -> list[tuple[str, bytes]]:
    paths = {path for path, _ in files}
    extra: list[tuple[str, bytes]] = []
    for slot in (0, 1):
        raw_name = slot_raw_name(slot)
        if raw_name in paths:
            continue
        legacy_name = slot_vmp_name(slot)
        legacy_data = next((data for path, data in files if path == legacy_name), None)
        if legacy_data is None:
            continue
        extra.append((raw_name, extract_raw_card(legacy_data)))
    return files + extra


def slot_hash_and_size(files: list[tuple[str, bytes]], slot: int) -> tuple[str, int] | None:
    raw = get_slot_raw_from_files(files, slot)
    if raw is None:
        return None
    return hashlib.sha256(raw).hexdigest(), len(raw)


def psp_visible_files(files: list[tuple[str, bytes]]) -> list[tuple[str, bytes]]:
    hidden = {slot_raw_name(0), slot_raw_name(1)}
    return [(path, data) for path, data in files if path not in hidden]


def psp_visible_stats(files: list[tuple[str, bytes]]) -> tuple[str, int, int]:
    visible = psp_visible_files(files)
    all_data = b"".join(data for _, data in visible)
    return hashlib.sha256(all_data).hexdigest(), sum(len(data) for _, data in visible), len(visible)


def create_vmp(raw: bytes) -> bytes:
    raw = extract_raw_card(raw)
    vmp = bytearray(VMP_SIZE)
    struct.pack_into("<I", vmp, 0x00, _VMP_MAGIC_LE)
    struct.pack_into("<I", vmp, 0x04, _VMP_OFFSET_LE)
    vmp[VMP_HEADER_SIZE:] = raw
    seed = bytes(vmp[_VMP_SEED_OFFSET : _VMP_SEED_OFFSET + 0x10])
    salt = bytearray(0x40)
    work = bytearray(_aes_decrypt_block(seed))
    salt[:0x10] = work
    work = bytearray(_aes_encrypt_block(seed))
    salt[0x10:0x20] = work

    for i in range(0x10):
        salt[i] ^= _VMP_AES_IV[i]

    work = bytearray(b"\xFF" * 0x14)
    work[:4] = vmp[_VMP_SEED_OFFSET + 0x10 : _VMP_SEED_OFFSET + 0x14]
    for i in range(0x10):
        salt[0x10 + i] ^= work[i] ^ _VMP_AES_IV[i]

    for i in range(0x14, len(salt)):
        salt[i] = 0
    for i in range(len(salt)):
        salt[i] ^= 0x36

    vmp[_VMP_HASH_OFFSET : _VMP_HASH_OFFSET + 0x14] = b"\x00" * 0x14
    inner_hash = hashlib.sha1(bytes(salt) + bytes(vmp)).digest()
    for i in range(len(salt)):
        salt[i] ^= 0x6A
    vmp[_VMP_HASH_OFFSET : _VMP_HASH_OFFSET + 0x14] = hashlib.sha1(bytes(salt) + inner_hash).digest()
    return bytes(vmp)
